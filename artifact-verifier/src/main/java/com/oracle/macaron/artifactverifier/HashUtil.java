package com.oracle.macaron.artifactverifier;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.security.MessageDigest;
import java.security.NoSuchAlgorithmException;

public class HashUtil {

    private static String bytesToHex(byte[] hash) {
        StringBuilder hexString = new StringBuilder(2 * hash.length);
        for (int i = 0; i < hash.length; i++) {
            String hex = Integer.toHexString(0xff & hash[i]);
            if(hex.length() == 1) {
                hexString.append('0');
            }
            hexString.append(hex);
        }
        return hexString.toString();
    }
    public static String getSHA256HashHexString(Path filepath) throws NoSuchAlgorithmException, IOException {
        MessageDigest sha256HashFunc = MessageDigest.getInstance("SHA-256");
        byte[] hash = sha256HashFunc.digest(Files.readAllBytes(filepath));
        String artifactSHA256Digest = bytesToHex(hash);
        return artifactSHA256Digest;
    }

}
